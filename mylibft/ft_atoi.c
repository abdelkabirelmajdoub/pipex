#include "libft.h"

int	ft_atoi(char *str)
{
	int				i;
	int				s;
	unsigned long	r;

	i = 0;
	s = 1;
	r = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13)) 
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while ((str[i] >= '0' && str[i] <= '9') && str[i])
	{
		if (r > 9223372036854775807 && s > 0)
			return (-1);
		if (r > 9223372036854775807 && s < 0)
			return (0);
		r = r * 10 + (str[i] - '0');
		i++;
	}
	return (r * s);
}
